	<h2>Difference between SAT and SAT 2 (Subject Test)</h2>
	<p>The difference is that the SAT tests student's critical thinking skills in Critical Reading, Math, and Writing necessary to succeed in college. The SAT Subject Test measures student's knowledge in particular subject areas closely linked to the high school curriculum and their ability to apply that knowledge.</p>

	<p>Some highly selective colleges require applicants to take SAT Subject Tests in addition to their SAT Exam. There are over 20 different Subject Areas that cover Literature, Math, Sciences, and Foreign Languages. Some schools will also consider the ACT as an alternative to both the SAT and some SAT Subject tests.</p>

	<em>Note: Before applying at any university check its SAT requirement (whether it requires SAT 1 or SAT 2 and if SAT 2, which subject(s) of SAT 2)</em>

